    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nomes, feedback, genero = "Nao Informado";
        nomes = nome.getText();
        feedback = feedbackField.getText();
        if(masculino.isSelected())
        {
            genero = masculino.getText();           
        }
        if(feminino.isSelected())
        {
            genero = feminino.getText();           
        }
        //armazenamento das plantas em TXT para outro exerc√≠cio: 
        FileWriter arquivo = null;
        try 
        {
            arquivo = new FileWriter("feedbacks.txt", true);
        } catch (IOException ex) 
        {
            Logger.getLogger(FormularioFeedback.class.getName()).log(Level.SEVERE, null, ex);
        }
        try (PrintWriter gravarArq = new PrintWriter(arquivo)) 
        {
            gravarArq.printf("%s;%s;%s\n", nomes, genero, feedback);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

